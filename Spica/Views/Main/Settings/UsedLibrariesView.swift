struct UsedLibrariesView: View {
	var body: some View {
		Group {
			if #available(iOS 14.0, *) {
				List {
					UsedLibrariesSubView()
				}.listStyle(InsetGroupedListStyle())
			} else {
				List {
					UsedLibrariesSubView()
				}.listStyle(GroupedListStyle())
					.environment(\.horizontalSizeClass, .regular)
			}
		}.navigationBarTitle(Text("Used Libraries"))
	}
}

struct UsedLibrariesSubView: View {
	var body: some View {
		ForEach(spicaAppLibraries) { library in
			VStack(alignment: .leading) {
				Text("\(library.name)").bold().font(.headline)
				Text("\(library.url.absoluteString)").foregroundColor(.secondary).font(.footnote)
			}.padding(4).onTapGesture {
				if UIApplication.shared.canOpenURL(library.url) {
					UIApplication.shared.open(library.url)
				}
			}
		}
		ForEach(Array(spicaAppLibraries.enumerated()), id: \.offset) { (index, library) in
			if index == spicaAppLibraries.count - 1 {
				Section(footer: Text("Thank you to all maintainers of these libraries!").padding()) {
					VStack(alignment: .leading) {
						Text("\(library.name)").bold().font(.headline)
						Text("\(library.license)").foregroundColor(.secondary).font(.footnote)
					}.padding(4)
				}
			}
			else {
				Section {
					VStack(alignment: .leading) {
						Text("\(library.name)").bold().font(.headline)
						Text("\(library.license)").foregroundColor(.secondary).font(.footnote)
					}.padding(4)
				}
			}
		}
	}
}

struct UsedLibrariesView_Previews: PreviewProvider {
    static var previews: some View {
        UsedLibrariesView()
    }
}

struct Library: Identifiable {
